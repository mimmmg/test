#include <stdio.h>
int main()
{
    int calc, x = 40, y = 60, z = 80;
    calc = x > y? y++ : --z;      // 앞에 붙으면 전위증가연산자 :++y++: 뒤에 붙으면 후위증가연산자 여기서 y는 후위증가니까 calc에 y값60 먼저 넣고 y+1해서 출력은 60//61
    printf("%d//%d//%d", calc, y ,z);
    return 0;
}

//calc = x < y ? y++ : --z; 이 줄이 핵심입니다:

//이것은 삼항 연산자를 사용한 조건문입니다.
//x < y가 참인지 거짓인지 평가합니다.
//참이면 y++를 실행하고, 거짓이면 --z를 실행합니다.


//printf("%d//%d//%d", calc, y, z);로 결과를 출력합니다.

//x < y는 40 < 60이므로 참입니다.
//따라서 y++가 실행됩니다.
//y++는 후위 증가 연산자로, y의 현재 값(60)을 calc에 할당한 후에 y를 1 증가시킵니다.

//그 결과:
//calc는 60이 됩니다.
//y는 61이 됩니다.
//z는 변하지 않고 80으로 남습니다.

//출력문에서:
//첫 번째 %d는 calc의 값 60을 출력합니다.
//두 번째 %d는 증가된 y의 값 61을 출력합니다.
//세 번째 %d는 변하지 않은 z의 값 80을 출력합니다.

//따라서 최종 출력은 "60//61//80"이 됩니다.