#include <stdio.h>
int main()
{
    int a [10] = {3, 7, 9, 4, 5, 1, 8, 2, 6, 10};
    int i;
    for (i =0; i < 10; i++)
    {
        if (i % 3 == 2)
        {
            printf("%d", a[i]);
        }
    }
}

//if 구문에서 3으로 나눈 나머지가 2인 i가 출력됨
//따라서 i는 0부터 9까지 돌아가니까 i가 2,5,8일 때 a[i]가 출력된다.
//따라서 a[2]=9 , a[5]=1, a[8]=6
